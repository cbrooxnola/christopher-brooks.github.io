function removeProperties(object, array) {
var objArr = Object.keys(object);
    for (var i = 0; i < array.length; i++) {
    for (var o = 0; o < objArr.length; o++) {
       if (objArr[o] === array[i]) {
        delete object[o];
       }
    }
}
}

