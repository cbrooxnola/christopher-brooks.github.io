// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var count = 0;
  var arr = string.split();
  for (var i = 0; i < arr.length; i++) {
    if (arr[i] === 'B') {
      count += count;
    }
    return count;
  }
  
}

