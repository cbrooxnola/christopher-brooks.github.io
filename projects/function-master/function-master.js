// Function 16 - Dedup ///////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function dedup(array) {
    var newArr = [];
    for (var i = 0; i < array.length; i++) {
        for (var o = 0; o < array.length; o++) {
           if (array[o] !== array[i]) {
            newArr.push(array[o]);
           }
        }
    }
    return newArr; 
}

