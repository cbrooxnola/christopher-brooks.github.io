////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var count = 0;
  var arr = string.split();
  for (var i = 0; i < arr.length; i++) {
    if (arr[i] === char) {
      count += count;
    }
    return count;
  }
}

